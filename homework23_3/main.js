// Запросить у пользователя трехзначное число и проверить, есть ли в нем одинаковые цифры.

let number = prompt("Введіть трьохзначне число");
number = number + "";
if (
  number[0] == number[1] ||
  number[0] == number[2] ||
  number[1] == number[2]
) {
  alert("Знайдено співпадіння цифр");
} else alert("Співпадіння цифр не знайдено");
